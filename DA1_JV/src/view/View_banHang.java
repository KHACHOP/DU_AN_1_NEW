    void loadSTHoaDon(String strTrangThai) {
        countHoaDon();
        if (counthd % 3 == 0) {
            tranghd = counthd / 3;
        } else {
            tranghd = counthd / 3 + 1;
        }
        loadHoaDon(1, strTrangThai);
        lbl_hoaDon.setText("1/" + tranghd);
    }

