    private void tbl_sanPhamMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbl_sanPhamMouseClicked
        int index = tbl_sanPham.getSelectedRow();
        String id_sanPham = tbl_sanPham.getValueAt(index, 0).toString();
        String soLuong = tbl_sanPham.getValueAt(index, 9).toString();
        String soLuongThem = "";
        soLuongThem = JOptionPane.showInputDialog("vui lòng nhập số lượng sản phẩm muốn thêm");
        try {
            Integer soLuongAdd = Integer.parseInt(soLuongThem);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Không được nhập số lượng là chữ");
            return;
        }

        Integer idNv = Integer.parseInt(txt_thongTinNv.getText());
        Double donGia = Double.parseDouble(tbl_sanPham.getValueAt(index, 7).toString());
        SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd");
        String ngayTao = sdf.format(txt_ngayTao.getDate());
        Integer id_khachHang = Integer.parseInt(txt_KhachHang.getText());
        if (String.valueOf(id_khachHang).isEmpty()) {
            JOptionPane.showMessageDialog(null, "vui lòng chọn hóa đơn cần thêm");
            return;
        } else {
            if (soLuongThem.isEmpty()) {
                JOptionPane.showMessageDialog(null, "vui lòng nhập số lượng muốn thêm");
                return;
            } else if (Integer.valueOf(soLuongThem) <= -1) {
                JOptionPane.showMessageDialog(null, "số lượng không được âm");
                return;
            } else if (Pattern.matches("[a-zA-Z]+", soLuongThem)) {
                JOptionPane.showMessageDialog(null, "Số lượng không phải là chữ");
                return;
            } else if (Integer.valueOf(soLuongThem) > Integer.valueOf(soLuong)) {
                JOptionPane.showMessageDialog(null, "số lượng không đủ");
                return;
            } else {
                banHang bh = new banHang();
                bh.setId_sanPham(Integer.parseInt(id_sanPham));
                bh.setSoLuongThem(Integer.valueOf(soLuongThem));
                bh.setGiaSp(Double.valueOf(donGia));
                bh.setId_khachHang(id_khachHang);
                bh.setSoLuong(Integer.parseInt(soLuong));
                bh.setNgayTao(ngayTao);
                bh.setId_nhanVien(idNv);
                service.themSanPham(bh);
                service.suaSoLuong(bh);
                loadSanPham(1);
                service.getListGioHang(idNv);
                loadGioHang(service.getGioHang(String.valueOf(id_khachHang)));
//                String search = txt_id_hoaDon.getText();
//                fillgioHang(search);
//                showTongTien();
            }
        }
    }//GEN-LAST:event_tbl_sanPhamMouseClicked

